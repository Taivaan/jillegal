package tr.com.serkanozal.jillegal.offheap.pool;

public class SampleOffHeapAggregatedClass {

	private boolean b;
	private char c;
	private float f;
	
	public boolean isB() {
		return b;
	}
	
	public void setB(boolean b) {
		this.b = b;
	}
	
	public char getC() {
		return c;
	}
	
	public void setC(char c) {
		this.c = c;
	}
	
	public float getF() {
		return f;
	}
	
	public void setF(float f) {
		this.f = f;
	}
	
}
